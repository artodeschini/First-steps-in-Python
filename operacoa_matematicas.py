import math

# teto arredonda para cima ou seja pega o proximo numero inteiro
print( math.ceil( 10.2 ) )

# piso arredonda para baixo ou seja trunca a parte decimal
print( math.floor( 10.2 ) )

# calculo do fatorial de um numero
print( math.factorial( 5 ) )

# potencia o metodo pow sempre retorna um float
print( math.pow(2,3) )
# pow pode ser substituido por **
print( 2 ** 3 )

# raiz quadrada
print( math.sqrt( 25 ) )

# maior divisor comum
print( math.gcd( 10, 5 ) )